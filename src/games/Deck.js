export class Deck
{
    constructor()
    {
        this.cards = [];
    }

    shuffle()
    {
        this.cards = [];
        for (const key in CARDS)
        {
            if (key !== 'back')
                this.cards.push(CARDS[key]);
        }

        for (let i = this.cards.length - 1; i > 0; i--)
        {
            const j = Math.floor(Math.random() * (i + 1));
            [this.cards[i], this.cards[j]] = [this.cards[j], this.cards[i]];
        }
    }

    draw()
    {
        return this.cards.pop();
    }
}
